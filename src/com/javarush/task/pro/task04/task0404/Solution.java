/* 
Заполненный прямоугольник
*/

public class Solution {
    public static void main(String[] args) {
        //напишите тут ваш код
        int stringCount = 0;

        while (stringCount < 5) {
            int columnCount = 0;

            while (columnCount < 10) {
                System.out.print("Q");
                columnCount++;
            }
            System.out.println();
            stringCount++;

        }

    }
}